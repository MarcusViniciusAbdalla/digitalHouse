// Simple Comparison

//  Equality Comparison ==

console.log(5==7);
console.log(5==5);

// Comparison of bigger >

console.log(5 > 7);
console.log(5 > 5);
console.log(7 > 5);

// Minor comparison <

console.log(5 < 7);
console.log(5 < 5);
console.log(7 < 5);

//Less than equal <=

console.log(5 <= 7);
console.log(5 <= 5);
console.log(7 <= 5);

//Greater than or equal comparison >=

console.log(5 >= 7);
console.log(5 >= 5);
console.log(7 >= 5);

// Hard equality comparison ===

console.log(5 === 7);
console.log(5 === 5);
console.log(7 === 5);
console.log(5 === "5");

// Comparison of different  !=

console.log(5 != 7);
console.log(5 != 5);
console.log(5 != "5");

// Rigid diff comparison !==  Analisa o valor e o tipo de dado

console.log(5 !== 7);
console.log(5 !== 5);
console.log(7 !== 5);
console.log(5 !== "5");

































