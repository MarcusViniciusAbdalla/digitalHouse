//Increment ++
let number = 10;
console.log(number++); //aparece o 10, mas já incrementou
console.log(number); //aparece o 11 porque manda imprimir o número que foi incrementado acima
console.log(++number); // aparece o 12

let number2 = 100
console.log(number2++); 
console.log(number2);
console.log(number2);
console.log(number2);

//Decrement --
let number3 = 10;
console.log(number3--); //aparece o 10, mas já decrementou
console.log(number3); //aparece o 09 porque manda imprimir o número que foi decrementado acima
console.log(--number3); // aparece o 08

let number4 = 100
console.log(number4--); 
console.log(number4);
